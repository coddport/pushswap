#include "includes/libft.h"

int		ft_isnum(char *s)
{
	int res;

	res = 0;
	while (ft_isspace(*s))
		s++;
	if (*s == '+' || *s == '-')
		s++;
	while (ft_isdigit(*s) && (res = 1))
		s++;
	return ((*s || !res) ? 0 : 1);
}
