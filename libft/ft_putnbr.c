#include <unistd.h>

void	ft_putnbr(int n)
{
	char x;

	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n = -n;
		}
		if (n >= 10)
			ft_putnbr(n / 10);
		x = (n % 10) + '0';
		write(1, &x, 1);
	}
}
