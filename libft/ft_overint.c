#include "includes/libft.h"

int		ft_overint(int n1, int n2)
{
	if (n1 > 0 && n2 > 0 && n1 > 2147483647 - n2)
		return (1);
	if (n1 < 0 && n2 < 0 && n1 < -2147483648 - n2)
		return (1);
	return (0);
}
